    private void jlDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlDeleteMouseClicked
        
        String id = tfID.getText();
        
        if(JOptionPane.showConfirmDialog(null, "Confirm to Delete", "Delete Account", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
            try {
                PreparedStatement ps = MyConnect.getConnection().prepareStatement("DELETE FROM `user` WHERE `id` = ?");
                ps.setString(1, id);


                int k = ps.executeUpdate();

                if(k==1){
                    JOptionPane.showMessageDialog(null, "Your Booker Account Has been Deleted");
                    this.dispose();
                    UserLogin l = new UserLogin();
                    l.setVisible(true);
                    l.pack();
                    l.setLocationRelativeTo(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Your Record Failed to Delete");
                }

            } catch (SQLException ex) {
                Logger.getLogger(registerBooker.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
    }//GEN-LAST:event_jlDeleteMouseClicked

