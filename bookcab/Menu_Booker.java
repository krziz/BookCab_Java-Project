    public ArrayList<User> userList() {
        ArrayList<User> userList = new ArrayList<>();
        
        PreparedStatement ps;
        ResultSet rs;
        String DriverQuery = "SELECT * FROM `driver`";
        
    
        try {
            
            //ps = MyConnect.getConnection().prepareStatement(DriverQuery);
            Statement st = MyConnect.getConnection().createStatement();
            rs = st.executeQuery(DriverQuery);
            User user;
            
            while(rs.next()) {
                user = new User(rs.getString("full_name"), rs.getString("address"), rs.getString("phone"), rs.getString("gender"), rs.getString("plateNumber"), rs.getString("carType"));
                userList.add(user);
            }
            
        } catch (SQLException ex) {
        Logger.getLogger(registerBooker.class.getName()).log(Level.SEVERE, null, ex);
        }
        return userList;       
    }
    
    public void showDriver() {
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)tableDriver.getModel();
        Object[] row = new Object[6];
        for(int i=0;i<list.size();i++) {
        
            row[0]=list.get(i).getfname();
            row[1]=list.get(i).getadd();
            row[2]=list.get(i).getphone();
            row[3]=list.get(i).getgender();
            row[4]=list.get(i).getpltNum();
            row[5]=list.get(i).getbrand();
            model.addRow(row);
    }
    }

