    //funtion for empty fields 
    public boolean verifyFields() {
        
        String uname = tfUser.getText();
        String fname = tfName.getText();
        String phone = tfPhone.getText();
        String address = tfAdd.getText();
        String plate = tfPlate.getText();
        String brand = tfBrand.getText();
        String pass = String.valueOf(jfPass.getPassword());
        String ConPass = String.valueOf(jfConPass.getPassword());
        
        //check empty fields
        if (fname.trim().equals("") || uname.trim().equals("") || address.trim().equals("") || plate.trim().equals("")
                || brand.trim().equals("")|| phone.trim().equals("")
                || pass.trim(). equals("") || ConPass.trim().equals("")) {
            
            JOptionPane.showMessageDialog(null, "One or More Fields Are Empty", "Empty Fields", 2);
            return  false;
        }
        //check password not match 
        else if (!pass.equals(ConPass)) {
            
            JOptionPane.showMessageDialog(null, "Password Doesn't Match", "Confirm Password", 2);
            return  false;
        }   
        //succesfull
        else {
            return true;
        }
    }
    
