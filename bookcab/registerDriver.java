    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        if(JOptionPane.showConfirmDialog(null, "Confirm if you want to Register into Driver", "Driver Register", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
            
            String fname = tfName.getText();
            String username = tfUser.getText();
            String pass = String.valueOf(jfPass.getPassword());
            //String conPass = String.valueOf(jfConPass.getPassword());
            String address = tfAdd.getText();
            String phone = tfPhone.getText();
            String gender = "Male";
            String platenumber = tfPlate.getText();
            String cartype = tfBrand.getText();

            if(rbFemale.isSelected()) {
                gender = "Female";
            }

            if(verifyFields()) {

                if(!checkUser(username)) {

                    PreparedStatement ps;
                    ResultSet rs;
                    String registerUserQuery = "INSERT INTO `driver`(`full_name`, `username`, `password`, `address`, `phone`, `gender`, `plateNumber`, `carType`) VALUES (?,?,?,?,?,?,?,?)";

                    try {
                        ps = MyConnect.getConnection().prepareStatement(registerUserQuery);
                        ps.setString(1, fname);
                        ps.setString(2, username);
                        ps.setString(3, pass);
                        ps.setString(4, address);
                        ps.setString(5, phone);
                        ps.setString(6, gender);
                        ps.setString(7, platenumber);
                        ps.setString(8, cartype);


                        try {

                            //ps.setNull(7, java.sql.Types.NULL);

                            if (ps.executeUpdate() != 0) {
                                JOptionPane.showMessageDialog(null, "Your Driver Account Has been Created");

                            } else {
                                JOptionPane.showMessageDialog(null, "Error! Check Your Information");
                            }
                        } catch (SQLException ex) {
                            Logger.getLogger(registerBooker.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(registerBooker.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            }
            
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

