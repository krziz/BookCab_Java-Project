    public boolean checkUser(String username) {
        
        PreparedStatement st;
        ResultSet rs;
        boolean user_exist = false;
        
        String query = "SELECT * FROM `driver` WHERE `username` = ? ";
        
        try {
            
            st = MyConnect.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next()) {
                user_exist = true;
                JOptionPane.showMessageDialog(null, "This Username is Already Taken,\n Chooose Another One.", "Username Failed", 2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(registerBooker.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return user_exist;
    } 
    
